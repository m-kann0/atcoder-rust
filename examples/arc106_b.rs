fn solve(input: &str) -> String {
    let mut iterator = input.split_whitespace();

    let n: usize = iterator.next().unwrap().parse().unwrap();
    let m: usize = iterator.next().unwrap().parse().unwrap();
    let a: Vec<isize> = (0..n).map(|_| iterator.next().unwrap().parse().unwrap()).collect();
    let b: Vec<isize> = (0..n).map(|_| iterator.next().unwrap().parse().unwrap()).collect();

    let mut dsu = Dsu::new(n);
    for _ in 0..m {
        let ci: usize = iterator.next().unwrap().parse().unwrap();
        let di: usize = iterator.next().unwrap().parse().unwrap();
        dsu.merge(ci - 1, di - 1);
    }

    for group in dsu.groups() {
        let mut sum_a = 0;
        let mut sum_b = 0;
        for u in group {
            sum_a += a[u];
            sum_b += b[u];
        }
        if sum_a != sum_b {
            return "No".to_string();
        }
    }
    "Yes".to_string()
}

